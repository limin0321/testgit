urGrade = int(input("ได้คะเเนนเก็บเท่าไหร่ : "))
if urGrade >= 80:
    print("ได้เกรด A จร้า")
elif urGrade >= 75:
    print("ได้เกรด B+ จร้า")
elif urGrade >= 70:
    print("ได้เกรด B จร้า")
elif urGrade >= 65:
    print("ได้เกรด C+ จร้า")
elif urGrade >= 60:
    print("ได้เกรด C จร้า")
elif urGrade >= 55:
    print("ได้เกรด D+ จร้า")
elif urGrade >= 50:
    print("ได้เกรด D จร้า")
else:
    print("ติด F!!!")
    